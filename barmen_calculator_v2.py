# День 2 — Упрощённая версия. Барменский калькулятор
import random

print("=== Барменский калькулятор смен v0.2 ===")

shifts = []   # Здесь будут храниться количества коктейлей за каждую смену

# Список любимых коктейлей
favorite_cocktails = ["Джин-тоник", "Авиация", "Джин-безил-смеш", "Маргарита", "Апероль"]
# Список мотивационных фраз
motivation_phrases = [
    "Ты настоящий бармен-машина! 🔥 Продолжай в том же духе!",
    "Каждый коктейль — это шаг к новой жизни. Горжусь тобой!",
    "От барной стойки до кода — ты уже на полпути. Не останавливайся!",
    "Такие результаты достойны повышения! 💰",
    "Сегодня ты был в ударе. Завтра будет ещё лучше!",
    "Помни: каждый смешанный коктейль приближает тебя к junior-разработчику 🐍"
]

print("Вводи количество коктейлей за каждую смену.")
print("Когда закончишь — напиши слово 'стоп'\n")

while True:
    answer = input("Коктейлей за смену: ")
    
    if answer.lower() == "стоп":
        break                     # выходим из цикла
    
    # Проверяем, что ввели число
    try:
        drinks = int(answer)
        
        if drinks < 0:
            print("X Количество коктейле не может быть отрицательным. Введи положительное число")
        else:
            shifts.append(drinks)     # добавляем в список если чисто коктейлей > 0
            print(f"✅ Добавлено {drinks} коктейлей")
            random_phrase = random.choice(motivation_phrases)       # выбираем случайную мотивационную фразу
            print(f"Мотивация на сегодня: {random_phrase}")

    except ValueError:
        print("❌ Введи целое число или слово 'стоп'")



# === После завершения ввода ===
if len(shifts) == 0:
    print("Ты не ввёл ни одной смены.")
else:
    count_shifts = len(shifts)                    # ← Вот здесь считается количество смен
    total_drinks = sum(shifts)                    # общее количество коктейлей
    average = total_drinks / count_shifts         # среднее за смену
    count_salary = count_shifts*4300              # расчет общего дохода без чаевых
    

# Расчёт общего дохода (зарплата + чаевые)


    tips = total_drinks * 800 * 0.15              # чаевые 15%
    count_total_earned = count_salary+tips        # расчет общего дохода вместе с чаевыми

    # === Случайный совет по любимому коктейлю ===
if len(favorite_cocktails) > 0:
    random_cocktail = random.choice(favorite_cocktails)   # выбираем случайный коктейль
    
    print("\n🎲 Случайный совет:")
    
    if random_cocktail == "Мохито":
        print(f"Сегодня стоит приготовить {random_cocktail}! Не забудь свежую мяту.")
    elif random_cocktail == "Маргарита":
        print(f"Классика! {random_cocktail} отлично идёт с солью на краю бокала.")
    elif random_cocktail == "Old Fashioned":
        print(f"Солидный выбор — {random_cocktail}. Крепко и по-мужски.")
    elif random_cocktail == "Негрони":
        print(f"{random_cocktail} — для тех, кто ценит горький вкус.")
    else:
        print(f"Попробуй сегодня {random_cocktail}! Это твой фаворит.")
    
    print("\n" + "="*40)
    print(f"За неделю ты отработал {count_shifts} смен")
    print(f"Всего приготовил {total_drinks} коктейлей")
    print(f"В среднем за смену: {average:.1f} коктейлей")
    print(f"Примерные чаевые за неделю: {tips:.0f} руб.")
    print(f"Заработная плата по кол-ву смен в неделю: {count_salary} руб.")
    print(f"Общее кол-во заработанных денег за неделю: {count_total_earned} руб.")
    print("="*40)

print("\nДень 2 завершён. Молодец!")